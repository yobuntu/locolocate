search.onchange = function () { createTempPopi(newDublinPopi) };

function createTempPopi(newPopi) {
    setTimeout(() => {
        let newPopiLayer = L.marker([newPopi.lat, newPopi.lng], {icon: newPopi.icon})
            .bindPopup(createPopiPopup(newPopi, true))
            .addTo(allLayers);
        newPopiLayer.openPopup();
    }, 300);
}

function tempToPopi(button) {
    button.classList.add('active');

    setTimeout(() => {
        map.removeLayer(zor);
        zor = L.geoJson(newPopiZor, {
            onEachFeature: onZorFeature,
        }).addTo(allLayers);
        zor.bringToBack();
    }, 1500)
}

